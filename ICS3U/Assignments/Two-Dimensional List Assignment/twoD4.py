# a function that given two 2D lists, will check if there is any overlap at any coordinate 
# (the position contains 1 on both lists)
# returns True if so, False otherwise

def collide(falling: list, fallen: list) -> bool:
        for i in range(len(falling)):
                for j in range(len(falling[0])):
                        if falling[i][j] == 1 and fallen[i][j] == 1:
                                return True
        return False

